package com.aca;

public class IntExtremes {

	public static void main(String[] args) {
		int positiveInt = 2_147_483_647;
		System.out.println(positiveInt);
		
		int one = 1;
		positiveInt += one;
		System.out.println(positiveInt);
		
		int negativeInt = -2_147_483_648;
		System.out.println(negativeInt);
		
		negativeInt -= one;
		System.out.println(negativeInt);
		
		
		 

	}

}
